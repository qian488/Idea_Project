package com.study.reflect;

public class Teacher {
    private String name;
    private int age;
    private String gender;
    private String address;
    private String phone;

    public Teacher() {
    }

    public Teacher(String name, int age, String gender, String address, String phone) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.address = address;
        this.phone = phone;
    }
}
